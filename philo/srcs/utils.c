#include "philo.h"

long	ft_atol(char *str)
{
	int		i;
	int		sign;
	long	value;

	value = 0;
	sign = 1;
	i = 0;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while ('0' <= str[i] && str[i] <= '9')
	{
		value = value * 10 + str[i] - '0';
		i++;
	}
	return (sign * value);
}
